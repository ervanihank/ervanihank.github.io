function initializeJournalStateFromQuery() {
  const params = new URLSearchParams(window.location.search);
  const countryParam = params.get("country");
  const queryParam = params.get("q");
  const creatorParam = params.get("creator");
  const scopeParam = params.get("scope");
  const minRatingParam = params.get("minRating");
  const releaseYearParam = params.get("year");
  const activityYearParam = params.get("activityYear");
  const savedCountry = sessionStorage.getItem("enk_journal_country");
  const savedCreator = sessionStorage.getItem("enk_journal_creator");
  const savedScope = sessionStorage.getItem("enk_journal_scope");

  if (countryParam) {
    state.country = countryParam;
  } else if (savedCountry) {
    state.country = savedCountry;
  }

  if (queryParam) {
    state.searchQuery = queryParam.toLowerCase();
  }

  if (creatorParam) {
    state.creatorQuery = creatorParam;
  } else if (savedCreator) {
    state.creatorQuery = savedCreator;
  }

  if (scopeParam && ["all", "book", "film"].includes(scopeParam)) {
    state.libraryScope = scopeParam;
  } else if (savedScope && ["all", "book", "film"].includes(savedScope)) {
    state.libraryScope = savedScope;
  }

  if (minRatingParam) {
    state.minRating = minRatingParam;
  }

  if (releaseYearParam) {
    state.releaseYear = releaseYearParam;
  }

  if (activityYearParam) {
    state.activityYear = activityYearParam;
  }
}

function t(key) {
  return translations[state.lang][key] || key;
}

function getLocalizedEntryTitle(entry, lang = state.lang) {
  const title = entry?.title || {};
  const englishOverride =
    (entry?.type === "book" && (englishBookTitleOverrides[entry?.goodreadsId] || englishBookTitleOverrides[title.tr])) || "";
  const englishTitle = englishOverride || title.en || "";
  const turkishTitle = title.tr || englishTitle || "";

  if (lang === "tr") {
    return turkishTitle || englishTitle || "";
  }

  if (entry?.type === "book") {
    return englishTitle || turkishTitle || "";
  }

  return englishTitle || turkishTitle || "";
}

function isMissingMetaValue(value) {
  const normalized = `${value || ""}`.trim().toLowerCase();
  return (
    normalized === "" ||
    normalized === "rating not recorded" ||
    normalized === "watch date not recorded" ||
    normalized === "read date not recorded" ||
    normalized === "puan kayıtlı değil" ||
    normalized === "puan kayitli degil" ||
    normalized === "izlenme tarihi kayıtlı değil" ||
    normalized === "izlenme tarihi kayitli degil" ||
    normalized === "okuma tarihi kayıtlı değil" ||
    normalized === "okuma tarihi kayitli degil"
  );
}

function getDisplayRating(value) {
  return isMissingMetaValue(value) ? t("unknownRating") : value;
}

function getDisplayActivityDate(entry) {
  const rawValue = entry.type === "film" ? entry.watchedDate : entry.readDate;
  const fallbackKey = entry.type === "film" ? "unknownWatchedDate" : "unknownReadDate";
  return isMissingMetaValue(rawValue) ? t(fallbackKey) : rawValue;
}

function applyLanguage() {
  document.documentElement.lang = state.lang;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = key === "footerText" ? getFooterText() : t(key);
  });

  document.querySelectorAll("[data-i18n-html]").forEach((node) => {
    const key = node.dataset.i18nHtml;
    node.innerHTML = t(key);
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    const key = node.dataset.i18nAriaLabel;
    node.setAttribute("aria-label", t(key));
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    node.setAttribute("placeholder", t(key));
  });

  langButtons.forEach((button) => {
    const isActive = button.dataset.langSwitch === state.lang;
    button.classList.toggle("is-active", isActive);
  });
}

function sortAlpha(values) {
  return [...values].sort((a, b) => a.localeCompare(b, state.lang));
}

function getEntriesByType(type, entries = journalEntries) {
  return entries.filter((entry) => entry.type === type);
}

function getEntriesForLibraryScope(scope) {
  if (scope === "book" || scope === "film") {
    return getEntriesByType(scope);
  }

  return journalEntries;
}

function groupedByCountry(entries) {
  const grouped = new Map();

  entries.forEach((entry) => {
    if (!grouped.has(entry.country)) {
      grouped.set(entry.country, []);
    }
    grouped.get(entry.country).push(entry);
  });

  return grouped;
}


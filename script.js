function countryKey(countryCode) {
  return `country${countryCode[0].toUpperCase()}${countryCode.slice(1)}`;
}

function humanizeCountryCode(countryCode) {
  if (!countryCode) {
    return "Unknown";
  }
  const spaced = countryCode
    .replace(/([a-z])([A-Z])/g, "$1 $2")
    .replace(/[-_]/g, " ")
    .trim();
  if (!spaced) {
    return "Unknown";
  }
  return spaced
    .split(/\s+/)
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join(" ");
}

function getCountryLabel(countryCode, entries) {
  const sample = entries.find((entry) => entry.country === countryCode);
  const customLabel = sample?.countryLabel?.[state.lang];
  if (customLabel && customLabel.toLowerCase() !== "unknown") {
    return customLabel;
  }
  const translated = t(countryKey(countryCode));
  if (translated && translated !== countryKey(countryCode)) {
    return translated;
  }
  return humanizeCountryCode(countryCode);
}


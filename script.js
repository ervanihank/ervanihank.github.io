function compactCountryValue(value) {
  return (value || "").toLowerCase().replace(/[^a-z0-9]/g, "");
}

function normalizeCountryCode(code) {
  if (!code) {
    return "unknown";
  }

  const compact = compactCountryValue(code);
  if (countryAliasToCanonical[compact]) {
    return countryAliasToCanonical[compact];
  }

  return code;
}

function normalizeEntryCountry(entry) {
  const normalizedCode = normalizeCountryCode(entry.country);
  const preferredLabel = canonicalCountryLabels[normalizedCode];

  if (!preferredLabel) {
    return entry;
  }

  return {
    ...entry,
    country: normalizedCode,
    countryLabel: {
      en: preferredLabel,
      tr: preferredLabel,
    },
  };
}


function buildCreatorAndYearFilters(entries) {
  const scopeEntries = getEntriesForLibraryScope(state.libraryScope);
  const creatorInput = document.querySelector("#filter-creator");
  const creatorLabel = document.querySelector("#filter-creator-label");
  const minRatingSelect = document.querySelector("#filter-min-rating");
  const releaseLabel = document.querySelector("#filter-release-label");
  const activityLabel = document.querySelector("#filter-activity-label");
  const releaseYearSelect = document.querySelector("#filter-release-year");
  const activityYearSelect = document.querySelector("#filter-activity-year");

  if (creatorLabel) {
    if (state.libraryScope === "book") {
      creatorLabel.textContent = t("creatorAuthor");
    } else if (state.libraryScope === "film") {
      creatorLabel.textContent = t("creatorDirector");
    } else {
      creatorLabel.textContent = t("creatorSearchLabel");
    }
  }

  if (creatorInput) {
    const creators = sortAlpha(
      [...new Set(scopeEntries.map((entry) => (entry.creator || "").trim()).filter(Boolean))]
    );

    const options = [`<option value="">${t("filterAnyOption")}</option>`];
    creators.forEach((creator) => {
      const count = scopeEntries.filter((entry) => (entry.creator || "").trim() === creator).length;
      options.push(`<option value="${escapeHtml(creator)}">${escapeHtml(creator)} (${count})</option>`);
    });

    if (state.creatorQuery && !creators.includes(state.creatorQuery)) {
      options.push(`<option value="${escapeHtml(state.creatorQuery)}">${escapeHtml(state.creatorQuery)}</option>`);
    }

    creatorInput.innerHTML = options.join("");
    creatorInput.value = state.creatorQuery || "";
  }

  if (releaseLabel) {
    if (state.libraryScope === "book") {
      releaseLabel.textContent = t("releaseYearLabelBook");
    } else if (state.libraryScope === "film") {
      releaseLabel.textContent = t("releaseYearLabelFilm");
    } else {
      releaseLabel.textContent = t("filterReleaseYearLabel");
    }
  }

  if (activityLabel) {
    if (state.libraryScope === "book") {
      activityLabel.textContent = t("activityYearLabelBook");
    } else if (state.libraryScope === "film") {
      activityLabel.textContent = t("activityYearLabelFilm");
    } else {
      activityLabel.textContent = t("filterActivityYearLabel");
    }
  }

  if (minRatingSelect) {
    const ratingOptions = ["", ...Array.from({ length: 10 }, (_, index) => `${(index + 1) / 2}`)];
    minRatingSelect.innerHTML = ratingOptions
      .map((value) => {
        if (value === "") {
          return `<option value="">${t("filterAnyOption")}</option>`;
        }
        return `<option value="${value}">${value}</option>`;
      })
      .join("");
    minRatingSelect.value = state.minRating;
  }

  if (releaseYearSelect) {
    const years = [...new Set(scopeEntries.map((entry) => parseEntryReleaseYear(entry)).filter((year) => year !== null))]
      .sort((a, b) => b - a)
      .map((year) => `${year}`);
    releaseYearSelect.innerHTML = [`<option value="">${t("filterAnyOption")}</option>`, ...years.map((year) => `<option value="${year}">${year}</option>`)].join("");
    releaseYearSelect.value = state.releaseYear;
  }

  if (activityYearSelect) {
    const currentYear = new Date().getFullYear();
    const activityYears = [];
    for (let year = currentYear; year >= 2000; year -= 1) {
      activityYears.push(year);
    }
    activityYearSelect.innerHTML = [`<option value="">${t("filterAnyOption")}</option>`, ...activityYears.map((year) => `<option value="${year}">${year}</option>`)].join("");
    activityYearSelect.value = state.activityYear;
  }
}

